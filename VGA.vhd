	if rising_edge(clk) then
		if (horizontal_cont >= Hsyncpulse+Hbackporch )
			and (horizontal_cont < Hsyncpulse+Hbackporch+Harea )
			and (vertical_cont >= Vsyncpulse+Vbackporch )
			and (vertical_cont < Vsyncpulse+Vbackporch+Varea )
